#include "libft.h"

int	ft_result(int a, int b, char c)
{
	int		res;

	if (c == '+')
		res = a + b;
	else if (c == '-')
		res = a - b;
	else if (c == '/')
		res = a / b;
	else if (c == '%')
		res = a % b;
	else
		res = a * b;
	return (res);
}
