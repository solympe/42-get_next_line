#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned char	*tmp;
	int				i;

	tmp = (unsigned char *)s;
	i = 0;
	while (n-- != 0)
	{
		tmp[i] = 0;
		i++;
	}
}
