int	ft_sqrt(int nb)
{
	int		i;

	i = 0;
	if (nb >= 0 && nb <= 2147483647)
	{
		while (i * i != nb)
		{
			if (i > nb / 2 || nb == 214386689)
				return (0);
			i++;
		}
	}
	return (i);
}
